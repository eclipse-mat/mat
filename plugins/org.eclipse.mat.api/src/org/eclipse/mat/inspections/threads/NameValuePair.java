package org.eclipse.mat.inspections.threads;

public class NameValuePair
{
    public String name;
    public String value;

    NameValuePair(String name, String value)
    {
        this.name = name;
        this.value = value;
    }

    public String getName()
    {
        return name;
    }

    public String getValue()
    {
        return value;
    }
}
